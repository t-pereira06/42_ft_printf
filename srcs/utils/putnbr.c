#include"../../ft_printf.h"

void	putnbr(int n)
{
	if (n > 2147483647)
		return ;
	if (n == -2147483648)
	{
		putchar('-');
		putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		putchar('-');
		n = n * -1;
	}
	if (n < 10)
	{
		putchar((n + 48));
		return ;
	}
	putnbr((n / 10));
	putchar(n % 10 + 48);
}
